void CGameObject::Collision(CCollider& other)
{
	if (GetActive() == false) return;
	if(collider) collider->CollisionCall(*this->collider, other);
	for (const auto& component : ComponetList)
		if (component->GetActive()) component->OnCollision(other);

	if (m_pSibling) m_pSibling->Collision(other);
	if (m_pChild) m_pChild->Collision(other);
}

bool CGameObject::CheckCollision(CCollider& other)
{
	return collider->bound->Intersects(*other.bound);
}

void CGameObject::AddComponet(CComponent* component)
{
	ComponetList.emplace_back(component);
}

CGameObject* CGameObject::GetChilde(string name)
{
	CGameObject* childe = nullptr;
	if (m_pSibling)
	{
		if (m_pSibling->name._Equal(name) || m_pSibling->name._Equal(m_pstrFrameName)) childe = m_pSibling;
		else childe = m_pSibling->GetChilde(name);

		if (childe) return childe;
	}
	if (m_pChild)
	{
		if (m_pChild->name._Equal(name) || m_pChild->name._Equal(m_pstrFrameName)) childe = m_pChild;
		else childe = m_pChild->GetChilde(name);

		if (childe) return childe;
	}

	return childe;
}

void CGameObject::SetAllColor(XMVECTOR& color)
{
	for (int i = 0; i < m_nMaterials; i++)
		m_ppMaterials[i]->SetColor(color);

	if (m_pSibling) m_pSibling->SetAllColor(color);
	if (m_pChild) m_pChild->SetAllColor(color);
}
